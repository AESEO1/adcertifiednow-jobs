// City names must start with a letter and contain only letters, spaces, hyphens, apostrophes, and periods
export function isValidCityName(city: string): boolean {
  return /^[a-zA-Z][a-zA-Z\s\-'.]{1,}$/.test(city.trim())
}

export function findStateBySlug(slug: string) {
  return US_STATES.find((s) => s.abbreviation.toLowerCase() === slug.toLowerCase()) ?? null
}
